     * @inheritdoc
     */
    public function rules()
    {
        return [
            [['id', 'user_id', 'created_at', 'updated_at'], 'integer'],
            [['address', 'place_id'], 'safe'],
            [['latitude', 'longitude'], 'number'],
        ];
    }

